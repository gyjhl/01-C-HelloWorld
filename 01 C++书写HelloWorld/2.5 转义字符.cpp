#include<iostream>
using namespace std;

int main2_5() {

	//转义字符

	//换行符 \n

	cout << "hello world\n" << endl;

	//反斜杠 \\

	cout << "\\" << endl;

	//水平制表符 \t 作用是可以整齐的输出数据

	cout << "aaa\thello world" << endl;
	cout << "aa\thello world" << endl;
	cout << "aaaaa\thello world" << endl;

	system("pause");

	return 0;

}